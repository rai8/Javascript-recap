//this is used for multiline statements
let color = "green";
switch (color) {
  case "red":
    console.log("The color is red");
    break;
  case "blue":
    console.log("The color is blue");
    break;
  case "green":
    console.log("The color is green");
  default:
    console.log("Your color in neither blue, green nor red");
    break;
}
